// relational operators - <, >, <=, >=, ==, !=, ===, !==
console.log( 1 == 1 );
console.log( 1 == '1' ); // true - avoid using ==

console.log( 1 === 1 ); // true (strict equality check - compares data types as well)
console.log( 1 === '1' ); // false - always use ===

