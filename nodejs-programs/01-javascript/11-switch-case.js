const city = 'Chennai';

switch( city ) {
    case 'Bangalore':
        console.log( 'Cubbon park' );
        break;
    case 'Chennai':
        console.log( 'Marina Beach' );
        break;
    case 'New Delhi':
        console.log( 'India Gate' );
        break;
    default:
        console.log( 'Unknown city' );
}